#include "libft.h"

long	ft_atol(const char *str)
{
	int		sign;
	long	out;

	while (ft_isspace(*str))
		str++;
	sign = 1;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	out = 0;
	while (ft_isdigit(*str))
	{
		out = out * 10 + *str - '0';
		str++;
	}
	return (out * sign);
}
